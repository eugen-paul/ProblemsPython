class Solution:
    def isSubsequence(self, s: str, t: str) -> bool:
        if len(s) == 0:
            return True
        if len(t) == 0:
            return False
        pos = 0
        for c in t:
            if c == s[pos]:
                pos += 1
            if pos == len(s):
                return True
        return False


def do_test(i: int, s, n, r):
    c = Solution()
    resp = c.isSubsequence(s, n)
    if resp == r:
        print("OK", i)
    else:
        print("NOK", i, "expected", r, "response", resp)


